function walkAndTranslate(node, langTranslations) {
    let translatedCount = 0;
    
    function processNode(currentNode) {
        // Пропускаем скрипты и стили
        if (currentNode.tagName === 'SCRIPT' || currentNode.tagName === 'STYLE') {
            return;
        }
        
        // Обрабатываем текстовые узлы
        if (currentNode.nodeType === Node.TEXT_NODE) {
            const text = currentNode.nodeValue.trim();
            if (text && langTranslations[text]) {
                currentNode.nodeValue = langTranslations[text];
                translatedCount++;
            }
            // Также пробуем варианты без лишних пробелов
            else if (text) {
                const normalized = text.replace(/\s+/g, ' ').trim();
                if (normalized !== text && langTranslations[normalized]) {
                    currentNode.nodeValue = langTranslations[normalized];
                    translatedCount++;
                }
            }
        }
        // Обрабатываем атрибуты элементов
        else if (currentNode.nodeType === Node.ELEMENT_NODE) {
            // Переводим атрибуты
            ['title', 'placeholder', 'alt', 'aria-label'].forEach(attr => {
                if (currentNode.hasAttribute(attr)) {
                    const attrValue = currentNode.getAttribute(attr);
                    if (attrValue && langTranslations[attrValue]) {
                        currentNode.setAttribute(attr, langTranslations[attrValue]);
                    }
                }
            });
            
            // Особый случай для заголовков с <br>
            if (currentNode.classList && 
                (currentNode.classList.contains('hero-title') || 
                 currentNode.classList.contains('section-title') ||
                 currentNode.classList.contains('product-title'))) {
                const html = currentNode.innerHTML;
                const normalized = html.replace(/<br\s*\/?>/gi, ' ').replace(/\s+/g, ' ').trim();
                if (langTranslations[normalized]) {
                    currentNode.innerHTML = langTranslations[normalized];
                    translatedCount++;
                }
            }
            
            // Рекурсивно обрабатываем дочерние элементы
            for (let i = 0; i < currentNode.childNodes.length; i++) {
                processNode(currentNode.childNodes[i]);
            }
        }
    }
    
    processNode(node);
    return translatedCount;
}