let currentLang = localStorage.getItem('everist-language') || 'ru';
let translationInProgress = false;

// Функция для получения перевода
function getTranslation(key, lang = currentLang) {
    return translations[lang] && translations[lang][key] ? translations[lang][key] : key;
}

// Основная функция перевода страницы
function translatePage(lang = currentLang) {
    if (translationInProgress) return;
    
    translationInProgress = true;
    
    try {
        currentLang = lang;
        localStorage.setItem('everist-language', lang);
        
        const langTranslations = translations[lang] || translations['en'];
        let elementsTranslated = 0;

        // Обходим весь документ
        elementsTranslated += walkAndTranslate(document.body, langTranslations);
        
        console.log(`🌐 Переведено элементов: ${elementsTranslated} на языке: ${lang}`);
        
        // Обновляем кнопку переключения языка
        updateLanguageButton(lang);
        
        // Обновляем заголовок документа
        document.title = lang === 'ru' ? 'Everist - Безводный уход за волосами' : 'Everist - Waterless Haircare';
        
        // Обновляем атрибут lang у html
        document.documentElement.lang = lang;
        
        // Добавляем анимацию переключения
        document.body.classList.add('language-changing');
        setTimeout(() => {
            document.body.classList.remove('language-changing');
        }, 300);
        
    } catch (error) {
        console.error('Ошибка при переводе:', error);
    } finally {
        translationInProgress = false;
    }
}

// Функция обновления кнопки языка
function updateLanguageButton(lang) {
    const langToggle = document.getElementById('langToggle');
    if (langToggle) {
        const langIcon = langToggle.querySelector('.lang-icon');
        if (langIcon) {
            langIcon.textContent = lang === 'ru' ? 'EN' : 'RU';
        }
        
        // Обновляем title кнопки
        langToggle.title = lang === 'ru' ? 'Switch to English' : 'Переключить на русский';
        
        // Анимация нажатия
        langToggle.classList.add('changing');
        setTimeout(() => {
            langToggle.classList.remove('changing');
        }, 300);
    }
}

// Функция переключения языка
function toggleLanguage() {
    const newLang = currentLang === 'ru' ? 'en' : 'ru';
    translatePage(newLang);
}

// Инициализация языка при загрузке страницы
function initLanguage() {
    // Устанавливаем язык из localStorage или определяем по браузеру
    const savedLang = localStorage.getItem('everist-language');
    const browserLang = navigator.language || navigator.userLanguage;
    
    if (!savedLang) {
        // Если язык не сохранен, определяем по браузеру
        if (browserLang.startsWith('ru')) {
            currentLang = 'ru';
        } else {
            currentLang = 'en';
        }
        localStorage.setItem('everist-language', currentLang);
    } else {
        currentLang = savedLang;
    }
    
    // Применяем язык
    translatePage(currentLang);
    
    // Добавляем обработчик клика на кнопку переключения языка
    const langToggle = document.getElementById('langToggle');
    if (langToggle) {
        langToggle.addEventListener('click', function(e) {
            e.preventDefault();
            toggleLanguage();
        });
        
        // Добавляем возможность переключения по Ctrl+Alt+L
        document.addEventListener('keydown', function(e) {
            if (e.ctrlKey && e.altKey && e.key.toLowerCase() === 'l') {
                toggleLanguage();
                e.preventDefault();
            }
        });
    }
}

